function testingSwitch(){
	var nilai = 10;
	
	switch(nilai){
		case 10:
			console.log('Dapat A');
			break;
		case 9:
			console.log('Dapat B');
			break;
		case 8:
			console.log('Dapat C');
			break;
		case 7:
			console.log('Dapat D');
			break;
		case 6:
			console.log('Dapat E');
			break;
		default:
			console.log('Dapat F');
}
}

testingSwitch();